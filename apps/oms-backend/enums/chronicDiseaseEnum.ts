export const getChronicDiseaseLabel = (keys) => {
  if (!keys || !Array.isArray(keys) || keys.length === 0) {
    return "无";
  }
  return keys
    .map((key) => chronicDiseaseDict[key]) // 尝试从慢性病信息对象中获取标签
    .filter((label) => label) // 过滤掉未定义或不存在的标签
    .join("、"); // 使用中文顿号连接多个标签
};
